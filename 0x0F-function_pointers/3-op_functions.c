#include "calc.h"
#include <stdio.h>
/**
 * op_add - a operator of a and b
 * @a: first operator
 * @b: second operator
 * Return: the result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - a operator of a and b
 * @a: first operator
 * @b: second operator
 * Return: the result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - a operator of a and b
 * @a: first operator
 * @b: second operator
 * Return: the result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - a operator of a and b
 * @a: first operator
 * @b: second operator
 * Return: the result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - a operator of a and b
 * @a: first operator
 * @b: second operator
 * Return: the result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
