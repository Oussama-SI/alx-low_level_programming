/**
 * add - operation betwen two numbers
 * @a: first number
 * @b: second number
 * Return: the result of operation
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - operation betwen two numbers
 * @a: first number
 * @b: second number
 * Return: the result of operation
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - operation betwen two numbers
 * @a: first number
 * @b: second number
 * Return: the result of operation
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - operation betwen two numbers
 * @a: first number
 * @b: second number
 * Return: the result of operation
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - operation betwen two numbers
 * @a: first number
 * @b: second number
 * Return: the result of operation
 */
int mod(int a, int b)
{
	return (a % b);
}
